def sumar(a, b):
    res = a + b
    print(f"el resultado de la suma es: {res}")

def restar(a, b):
    res = a - b
    print(f"el resultado de la resta es: {res}")

def multiplicar(a, b):
    res = a * b
    print(f"el resultado de la multiplicacion es: {res}")

def dividir(a, b):
    res = a / b
    print(f"el resultado de la division es: {res}")